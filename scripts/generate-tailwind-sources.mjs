#!/usr/bin/env node
/**
 * Windows WDAC blocks @tailwindcss/oxide native binaries. The WASI fallback
 * cannot glob-scan the filesystem, so Tailwind v4 generates almost no utilities.
 *
 * This script scans TS/TSX/Blade files with Node.js and writes @source inline()
 * directives that Tailwind can consume without oxide file scanning.
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const ROOT = path.resolve(import.meta.dirname, '..');
const OUT = path.join(ROOT, 'resources/css/generated-sources.css');

const SCAN_DIRS = [
    path.join(ROOT, 'resources/js'),
    path.join(ROOT, 'resources/views'),
];

const EXTENSIONS = new Set(['.tsx', '.ts', '.blade.php']);

function walk(dir, files = []) {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) {
            if (entry.name === 'node_modules') continue;
            walk(full, files);
            continue;
        }
        const ext = entry.name.endsWith('.blade.php')
            ? '.blade.php'
            : path.extname(entry.name);
        if (EXTENSIONS.has(ext)) files.push(full);
    }
    return files;
}

function extractClasses(content) {
    const classes = new Set();

    const addTokens = (raw) => {
        for (const token of raw.split(/\s+/)) {
            const cleaned = token.replace(/["'`]/g, '').trim();
            if (!cleaned || cleaned.includes('${')) continue;
            if (/^[a-zA-Z0-9_\-:/[\].!%#(),]+$/.test(cleaned)) {
                classes.add(cleaned);
            }
        }
    };

    for (const match of content.matchAll(/className\s*=\s*"([^"]*)"/g)) {
        addTokens(match[1]);
    }
    for (const match of content.matchAll(/className\s*=\s*'([^']*)'/g)) {
        addTokens(match[1]);
    }
    for (const match of content.matchAll(/className\s*=\s*`([^`]*)`/g)) {
        addTokens(match[1]);
    }
    for (const match of content.matchAll(/\bcn\(([^)]*)\)/gs)) {
        for (const sm of match[1].matchAll(/["'`]([^"'`]*)["'`]/g)) {
            addTokens(sm[1]);
        }
    }
    for (const match of content.matchAll(/\bclass\s*=\s*"([^"]*)"/g)) {
        addTokens(match[1]);
    }

    return classes;
}

const allClasses = new Set();
let fileCount = 0;

for (const dir of SCAN_DIRS) {
    if (!statSync(dir, { throwIfNoEntry: false })?.isDirectory()) continue;
    for (const file of walk(dir)) {
        fileCount++;
        const content = readFileSync(file, 'utf8');
        for (const cls of extractClasses(content)) {
            allClasses.add(cls);
        }
    }
}

const sorted = [...allClasses].sort();
const CHUNK_SIZE = 400;
const chunks = [];

for (let i = 0; i < sorted.length; i += CHUNK_SIZE) {
    chunks.push(sorted.slice(i, i + CHUNK_SIZE).join(' '));
}

const header = `/* Auto-generated by scripts/generate-tailwind-sources.mjs — do not edit */\n`;
const body = chunks
    .map((chunk) => `@source inline("${chunk.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}");`)
    .join('\n');

writeFileSync(OUT, `${header}${body}\n`, 'utf8');

console.log(
    `Generated ${sorted.length} Tailwind candidates from ${fileCount} files → ${path.relative(ROOT, OUT)}`,
);
